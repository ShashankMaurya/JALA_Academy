package lab3_Loops;

public class Q4 {
    public static void main(String[] args) {
        System.out.print("Odd nos.: ");
        for(int i=1;i<20;i+=2)
            System.out.print(i+" ");
        System.out.println();
        System.out.print("Even nos.: ");
        for(int i=2;i<20;i+=2)
            System.out.print(i+" ");
    }
}
